    void personalizarTablaCierre() {
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date date = new Date();
        this.jLabel_horaCierre.setText(dateFormat.format(date));
        Direct_Control_BD AdminBD = Direct_Control_BD.getInstance();
        int idCierreVigente= AdminBD.obtenerultimoidCierre();
        String fechaInicio = AdminBD.obtenerFechaInicioCierre(idCierreVigente);
        this.jLabel_horaInicio.setText(fechaInicio);        
        String[] columnNames = {"Fecha","Concepto",
            "Num Fact","Tipo Pago","Monto"};
        List<Object[]> data = new ArrayList<Object[]>();      
        //Agrega el modelo a la factura
        Modelo_CierreCaja model=new Modelo_CierreCaja(columnNames,data);
        this.jTable_VerCierre.setModel(model);
        this.cargarListenerRenders();
        this.cargarFacturasCierre(model);
        this.cargarApartadoCreditosCierre(model);
        this.cargarDevolucionesCierre(model);
         //Agrega 20 filas
        model.addRow(20);
        
    }

    private void cargarFacturasCierre(Modelo_CierreCaja model) {
       
        Direct_Control_BD AdminBD = Direct_Control_BD.getInstance();
        AdminBD.verCierreFacturasContado(this.jLabel_horaInicio.getText(),this.jLabel_horaCierre.getText());
        Object[][] InfoFactsContadoCierre = AdminBD.getData();
        int numFilas = InfoFactsContadoCierre.length;
        for (int row = 0; row < numFilas; row++) {
            model.addRow(1);
            Object[] infofactscontado= InfoFactsContadoCierre[row];
            String Fecha= infofactscontado[0].toString();
            String Concepto= infofactscontado[1].toString();
            int NumFact = Integer.parseInt(infofactscontado[2].toString());
            String TipoPago=infofactscontado[3].toString();
            BigDecimal monto= this.StringtoBigDecimal(infofactscontado[4].toString());
            model.setValueAt(Fecha, row, 0);
            model.setValueAt(Concepto, row, 1);
            model.setValueAt(NumFact, row, 2);
            model.setValueAt(TipoPago, row, 3);
            model.setValueAt(monto, row, 4);
            
            
            }
    
    }
