            model.setValueAt(Fecha, numFilasTablas+row, 0);
            model.setValueAt(Concepto, numFilasTablas+row, 1);
            model.setValueAt(NumFact, numFilasTablas+row, 2);
            model.setValueAt(TipoPago, numFilasTablas+row, 3);
