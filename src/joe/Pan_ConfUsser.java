    private void crearPersona(String tipoUsuario) {
        String nombre = jFormattedTextField_nombreAdm.getText();
        String direccion = jFormattedTextField_direccionAdm.getText();
        String telefono = jFormattedTextField_telefonoAdm.getText();
        String fechaCumpleanos = dateF.format(
                dateChooserCombo_CumpCrearVendAdm.getSelectedDate().
                getTime());
        String clave = jPasswordField_Adm.getText().trim();
        if (fechaCumpleanos.equals("")) {
            fechaCumpleanos = null;
        }
        //si se crea la persona 
        if (AdminBD.insertarPersona(nombre, direccion, telefono,
                fechaCumpleanos)) {
            AdminBD.insertarUsuario(tipoUsuario, clave);
        }
    }

    private void modificarPersona(String tipoUsuario, String nombre) {
        String nuevoNombre = this.jFormattedTextField_Modifnombre.getText();
        String direccion = this.jFormattedTextField_Modifdireccion.getText();
        String telefono = this.jFormattedTextField_Modiftelefono.getText();
        String fechaCumpleanos = dateF.format(
                dateChooserCombo_CumpModfVendAdm.getSelectedDate().
                getTime());
        String clave = this.jPasswordField_Modif.getText().trim();
        if (fechaCumpleanos.equals("")) {
            fechaCumpleanos = null;
        }
        AdminBD.modificarUsuario(nombre, direccion, telefono,
                fechaCumpleanos, clave, tipoUsuario, nuevoNombre);
    }

    private boolean cargarDatosModf() {

        try {
            if ("Administrador".equals(jTable_Generica_AdmVend.getValueAt(
                    jTable_Generica_AdmVend.getSelectedRow(), 4).toString())) {

                AdminBD.ConsultarContrasena(jTable_Generica_AdmVend.getValueAt(
                        jTable_Generica_AdmVend.getSelectedRow(), 0).toString(),
                        "Administrador");
                jPasswordField_Modif.setText(AdminBD.getData()[0][0].toString());
            } else {

                AdminBD.ConsultarContrasena(jTable_Generica_AdmVend.getValueAt(
                        jTable_Generica_AdmVend.getSelectedRow(), 0).toString(),
                        "Vendedor");
                jPasswordField_Modif.setText(AdminBD.getData()[0][0].toString());
            }
            jFormattedTextField_Modifnombre.setText(jTable_Generica_AdmVend.
                    getValueAt(
                            jTable_Generica_AdmVend.getSelectedRow(), 0).toString());
            jFormattedTextField_Modifdireccion.setText(jTable_Generica_AdmVend.
                    getValueAt(
                            jTable_Generica_AdmVend.getSelectedRow(), 1).toString());
            jFormattedTextField_Modiftelefono.setText(jTable_Generica_AdmVend.
                    getValueAt(
                            jTable_Generica_AdmVend.getSelectedRow(), 2).toString());
            Calendar calendar = new GregorianCalendar();
            try {
                //Establecer fecha de cumpleanos
                Date date = dateFormat.parse(jTable_Generica_AdmVend.getValueAt(
                        jTable_Generica_AdmVend.getSelectedRow(), 3).toString());
                calendar.setTime(date);
                dateChooserCombo_CumpModfVendAdm.setSelectedDate(calendar);

            } catch (ParseException e) {
                //Si la fecha es nula pone fecha actual
                calendar.setTime(new Date());
                dateChooserCombo_CumpModfVendAdm.setSelectedDate(calendar);
            }

        } catch (Exception e) {

        }
        return false;
    }

