    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        int selection = jTable1.getSelectedRow();
        if (selection == LISTA_PRECIO){
            listaDePrecio();
        } else if (selection == LISTA_PRECIO_COSTO){
            listaDePrecioCosto();
        } else if (selection == MOV_INVENTARIO){
            movimientoInventario();
        } else if (selection == MOV_PRODUCTO){
            movimientosPorProducto();
        } else if (selection == PROD_AGOTADOS){
            productosAgotados();
        } else if (selection == PROD_INVENTARIO){
            productosEnInventario();
        } else if (selection == VALOR_INVENTARIO){
            valorDelInventario();
        }
    }//GEN-LAST:event_jLabel14MouseClicked

